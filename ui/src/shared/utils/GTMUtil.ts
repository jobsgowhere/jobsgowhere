import useGTM from "@elgorditosalsero/react-gtm-hook";

export const EVENT_NAME_NEW_POST = "new_post";

export const trackEvent = (eventName: string, eventValue: string | null) => {
  const { sendDataToGTM } = useGTM();
  console.log("GTMUtil - trackEvent: " + eventName + " val = " + eventValue);
  sendDataToGTM({ event: eventName, value: eventValue });
};
